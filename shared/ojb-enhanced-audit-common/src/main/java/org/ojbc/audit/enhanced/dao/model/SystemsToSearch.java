package org.ojbc.audit.enhanced.dao.model;

public class SystemsToSearch {

	private Integer systemsToSearchId;
	private String systemUri;
	private String systemName;

	public Integer getSystemsToSearchId() {
		return systemsToSearchId;
	}

	public void setSystemsToSearchId(Integer systemsToSearchId) {
		this.systemsToSearchId = systemsToSearchId;
	}

	public String getSystemUri() {
		return systemUri;
	}

	public void setSystemUri(String systemUri) {
		this.systemUri = systemUri;
	}

	public String getSystemName() {
		return systemName;
	}

	public void setSystemName(String systemName) {
		this.systemName = systemName;
	}
	
}
