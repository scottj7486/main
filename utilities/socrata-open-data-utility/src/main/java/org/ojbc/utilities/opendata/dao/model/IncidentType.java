package org.ojbc.utilities.opendata.dao.model;

public class IncidentType {

	private String incidentTypeDescription;
	
	private String incidentCategoryDescription;

	public String getIncidentTypeDescription() {
		return incidentTypeDescription;
	}

	public void setIncidentTypeDescription(String incidentTypeDescription) {
		this.incidentTypeDescription = incidentTypeDescription;
	}

	public String getIncidentCategoryDescription() {
		return incidentCategoryDescription;
	}

	public void setIncidentCategoryDescription(String incidentCategoryDescription) {
		this.incidentCategoryDescription = incidentCategoryDescription;
	}
}
