package org.ojbc.utilities.opendata.dao.model;

public class Charge {

	private String arrestChargeStatute;
	private String involvedDrugDescription;
	
	public String getArrestChargeStatute() {
		return arrestChargeStatute;
	}

	public void setArrestChargeStatute(String arrestChargeStatute) {
		this.arrestChargeStatute = arrestChargeStatute;
	}

	public String getInvolvedDrugDescription() {
		return involvedDrugDescription;
	}

	public void setInvolvedDrugDescription(String involvedDrugDescription) {
		this.involvedDrugDescription = involvedDrugDescription;
	}
	
}
