package org.springframework.ldap.samples.plain.web;

import java.io.Serializable;

import javax.validation.constraints.AssertTrue;

import org.apache.commons.lang.StringUtils;
import org.apache.commons.lang.builder.EqualsBuilder;
import org.apache.commons.lang.builder.HashCodeBuilder;
import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;
import org.hibernate.validator.constraints.NotBlank;

/**
 * POJO to take the input from the update password form.
 */
public class ChangePasswordCommand  implements Serializable{

	private static final long serialVersionUID = 2010620626583994006L;
	
	@NotBlank
	private String userId;
	@NotBlank
    private String oldPassword;

	@NotBlank
    private String newPassword; 
	@NotBlank
    private String confirmNewPassword; 
    
    public boolean equals(Object obj) {
        return EqualsBuilder.reflectionEquals(
                this, obj);
    }

    public int hashCode() {
        return HashCodeBuilder
                .reflectionHashCode(this);
    }

    public String toString() {
        return ToStringBuilder.reflectionToString(
                this, ToStringStyle.MULTI_LINE_STYLE);
    }

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getOldPassword() {
		return oldPassword;
	}

	public void setOldPassword(String oldPassword) {
		this.oldPassword = oldPassword;
	}

	public String getNewPassword() {
		return newPassword;
	}

	public void setNewPassword(String newPassword) {
		this.newPassword = newPassword;
	}

	public String getConfirmNewPassword() {
		return confirmNewPassword;
	}

	@AssertTrue(message="the new passwords must match")
	public boolean isMatchingPassword(){
		return StringUtils.equals(newPassword,confirmNewPassword);
	}
	public void setConfirmNewPassword(String confirmNewPassword) {
		this.confirmNewPassword = confirmNewPassword;
	}

}
